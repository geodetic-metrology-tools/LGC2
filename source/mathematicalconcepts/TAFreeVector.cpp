TAFreeVector::TAFreeVector()
{	// default constructor
	fStatus = kNull;
}



TAFreeVector::~TAFreeVector()
{
}

//////////////////////////////////////////////////////////////////////
// Member Functions
//////////////////////////////////////////////////////////////////////


void TAFreeVector::setStatus(TAFreeVector::EVectorStatus stat)
{/// set status
	fStatus = stat;
}


bool TAFreeVector::isNull() const
{/// get Status
	if (fStatus == TAFreeVector::kNull)
		{return true;}
	else
		{return false;}
}
