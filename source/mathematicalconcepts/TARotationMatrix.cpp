TARotationMatrix::TARotationMatrix()
{	// default constructor
	setStatus(kNull);
}


TARotationMatrix::~TARotationMatrix()
{
}

//////////////////////////////////////////////////////////////////////
// Member Functions
//////////////////////////////////////////////////////////////////////




void TARotationMatrix::setStatus(TARotationMatrix::ERotationStatus stat)
{/// set status
	fStatus = stat;
}


bool TARotationMatrix::isNull() const
{/// get Status
	if (fStatus == TARotationMatrix::kNull)
		{return true;}
	else
		{return false;}
}
