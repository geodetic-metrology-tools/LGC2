/////////////////////////////////////////////////////////////////////////////
//constructor / destructor
/////////////////////////////////////////////////////////////////////////////
//default constructor
TAFileWriter::TAFileWriter() : TALGCConverter(), fProjectData (nullptr), fError("")
{
}

//constructor
TAFileWriter::TAFileWriter(TAStreamFormatter* stream, const TLGCData* projectData) : TALGCConverter(*stream), fProjectData (projectData), fError("")
{
}


TAFileWriter::~TAFileWriter()
{//destructor
	getStream()->close();
}



//////////////////////////////////////////////////////////////////////////////////////////////////////////
// MEMBER PUBLIC FUNCTIONS
//////////////////////////////////////////////////////////////////////////////////////////////////////////
void	TAFileWriter::writeFile(const string error)
{//write error messages from project
	writeError();
}



///////////////////////////////////////////////////////////
//ERROR
///////////////////////////////////////////////////////////
string	TAFileWriter::getError() const
{//ERROR
	return fError;
}


void	TAFileWriter::setError(const string error)
{
	fError =fError + error +'\n';
	return;
}


void	TAFileWriter::writeError()
{
	(*getStream())<<fError;
	return;
}
